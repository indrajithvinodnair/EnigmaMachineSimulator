package com.wehrmacht.dataModels;

public class RotorDataModel {
    String rotorIdentifier;
    String ringSetting;
    String rotorPosition;

    public String getRotorIdentifier() {
        return rotorIdentifier;
    }

    public void setRotorIdentifier(String rotorIdentifier) {
        this.rotorIdentifier = rotorIdentifier;
    }

    public String getRotorPosition() {
        return rotorPosition;
    }

    public void setRotorPosition(String rotorPosition) {
        this.rotorPosition = rotorPosition;
    }

    public String getRingSetting() {
        return ringSetting;
    }

    public void setRingSetting(String ringSetting) {
        this.ringSetting = ringSetting;
    }
}
